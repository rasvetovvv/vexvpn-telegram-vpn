def plans_block(plans) -> str:
    """Текстовый список тарифов для экрана выбора (источник — БД тарифов)."""
    lines = []
    for p in plans:
        if not p.visible:
            continue
        lines.append(
            f"• <b>{p.title}</b> — ⭐ {p.stars} · {p.days} дн. · {p.traffic_label} · {p.devices_label} {p.badge}"
        )
    return "\n".join(lines)
